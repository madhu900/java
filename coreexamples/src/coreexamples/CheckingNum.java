package coreexamples;

public class CheckingNum {

	public static void main(String[] args) {
		float num = 123.000f;
		if(num>0) {
			System.out.println("positive");
		}else if(num<0) {
			System.out.println("negative");
		}else {
			System.out.println("zero");
			
			
		}

	}

}
